    private Thread t;

    private void startSpeed() {

        timeComboBox.setEnabled(false);
        t = new Thread() {
            @Override
            public void run() {

                long startTime = new Date().getTime();
                long endTime = new Date().getTime();
                int difTime = Integer.parseInt((String) timeComboBox.getSelectedItem());
                long timeDif = difTime * 1000;

                while ((endTime - startTime) < timeDif) {
                    endTime = new Date().getTime();
                    statusBar.setValue((int) ((endTime - startTime) / (difTime * 10)));
                }
                showPopUp();
            }
        };
        t.start();
    }

    private void showPopUp() {
        mainPanel.setBackground(new java.awt.Color(51, 51, 51));
        popUpDialog.setLocationRelativeTo(mainPanel);
        popUpDialog.setVisible(true);
        this.setEnabled(false);

        float speed = (float) speedClick / Integer.parseInt((String) timeComboBox.getSelectedItem());

        String speedString = String.valueOf(Math.round(speed * 100.0) / 100.0);
        speedLabel.setText(speedString + "  Clicks / Second");
        clickLabel.setText(String.valueOf(speedClick));
        timeLabel.setText((String) timeComboBox.getSelectedItem());
        resultLabel.setText(getResult(speed));
        speedClick = 0;
    }

    private String getResult(float speed) {
        String result = "";
        if (speed > 20) {
            result = "!!! CLICK GOD !!!";
        } else if (speed >= 15) {
            result = "KILLER CLICKER";
        } else if (speed >= 13) {
            result = "SUPER HUMAN !!!";
        } else if (speed >= 12) {
            result = "CLICK NINJA";
        } else if (speed >= 10) {
            result = "PRO CLICKER";
        } else if (speed >= 8) {
            result = "CLICK MASTER";
        } else if (speed >= 5) {
            result = "YOUNG CLICKER";
        } else if (speed >= 3) {
            result = "BABY CLICK";
        } else {
            result = "STILL LEARNING !";
        }
        return result;
    }

    private void hidePopUp() {
        popUpDialog.setVisible(false);
        this.setEnabled(true);
        this.setExtendedState(NORMAL);
        this.requestFocus();
        firstClick = true;
        timeComboBox.setEnabled(true);
    }


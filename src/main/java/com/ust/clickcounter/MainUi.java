    private boolean firstClick = true;

    private void mainPanelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mainPanelMousePressed
        // TODO add your handling code here:     
        totalClickCountLabel.setText(String.valueOf(++totalClickCount));
        ++speedClick;
        mainPanel.setBackground(new java.awt.Color(204, 166, 166));

        switch (evt.getButton()) {
            case 1:
                leftClickCountLabel.setText(String.valueOf(++leftClickCount));
                break;
            case 2:
                middleClickCountLabel.setText(String.valueOf(++middleClickCount));
                break;
            case 3:
                rightClickCountLabel.setText(String.valueOf(++rightClickCount));
                break;
            default:
                System.err.println("Invalid key");
        }
        if (firstClick) {
            startSpeed();
            firstClick = false;
        }
    }//GEN-LAST:event_mainPanelMousePressed

